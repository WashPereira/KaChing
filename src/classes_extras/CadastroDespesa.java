package classes_extras;

public class CadastroDespesa {
	public String nomedespesa;
	public String venc_despesa;
	public String descr_depesa;
	public String qtd_parcela;
	public String valor_depesa;
	public String getNomedespesa() {
		return nomedespesa;
	}
	public void setNomedespesa(String nomedespesa) {
		this.nomedespesa = nomedespesa;
	}
	public String getVenc_despesa() {
		return venc_despesa;
	}
	public void setVenc_despesa(String venc_despesa) {
		this.venc_despesa = venc_despesa;
	}
	public String getDescr_depesa() {
		return descr_depesa;
	}
	public void setDescr_depesa(String descr_depesa) {
		this.descr_depesa = descr_depesa;
	}
	public String getQtd_parcela() {
		return qtd_parcela;
	}
	public void setQtd_parcela(String qtd_parcela) {
		this.qtd_parcela = qtd_parcela;
	}
	public String getValor_depesa() {
		return valor_depesa;
	}
	public void setValor_depesa(String valor_depesa) {
		this.valor_depesa = valor_depesa;
	}

}
